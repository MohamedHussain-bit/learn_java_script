// math object
// Math.round() round the number
console.log(Math.round(99.5));
// Math.cile() round the number in the top
console.log(Math.ceil(99.1));
// Math.floor() round the number in the botum
console.log(Math.floor(100.9));
// Math.min() return min number
console.log(Math.min(1,2,3,4,5,6));
// Math.max() return max number
console.log(Math.max(1,2,3,4,5,6));
// Math.pow() return power of the number
console.log(Math.pow(5,2));
// Math.random() return random dicimal number
console.log(Math.random());
// Math.trunc() cut the number from .
console.log(Math.trunc(100.500));
// Math.sqrt() return the number sqrt
console.log(Math.sqrt(100));
// Math.abs() return the number positive
console.log(Math.abs(-100));
