// array methods
let user = ["Ahmed" , "Mohamed" , "Osama" , "Marawan" , "Ali"]
console.log(user)
// unshift() add element to the first
user.unshift("Amir" , "Nabile")
console.log(user)
// push() add element to the end
user.push("Mena" , "Yassmen")
console.log(user)
//  shift() remove first element to the array
user.shift()
console.log(user)
